const producto = {
      nombre: "Monitor 20 Pulgadas",
      precio: 300,
      disponible: true,     
}



// METODOS PARA OBJETOS

// Keys  --> Sirve para obtener los keys o las propiedades del objeto
console.log(Object.keys(producto));


// Values --> Sirve para obtener los valores de las propiedades.

console.log(Object.values(producto));


// Entries  --> Retorna todo en pares es decir (su propiedad y su valor)
console.log(Object.entries(producto));